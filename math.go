package main                                                                    
                                                                                
import "fmt"                                                                    
                                                                                
func main () {                                                                  
		//Addition
		fmt.Println(5+5)
		//Subtraction
		fmt.Println(7-5)
		//Multplication
		fmt.Println(5*5)
		//Division
		fmt.Println(10/3)
		//Modulus = Returns the remainder after division!
		fmt.Println(10%3)        
		//Incremenet
		number := 10
		number ++
		fmt.Println(number)        
		//Decremenet
		number1 := 10
		number1 --
		fmt.Println(number1)        
                                   
} 